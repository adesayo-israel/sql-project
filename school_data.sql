DROP TABLE IF EXISTS tblEnrolments
DROP TABLE IF EXISTS tblStudents
DROP TABLE IF EXISTS tblModules
DROP TABLE IF EXISTS tblCourses

--Create courses table
CREATE TABLE tblCourses(
	CourseCode nvarchar(4) NOT NULL,
	CourseName nvarchar(20) NULL
)

--Create students table
CREATE TABLE tblStudents(
	StudentNo int NOT NULL,
	Surname nvarchar(20) NULL,
	Forenames nvarchar(20) NULL,
	CourseCode nvarchar(4) NULL,
	PAT nvarchar(25) NULL
) 

--Create modules table
CREATE TABLE tblModules(
	GroupNo nvarchar(4)NOT NULL,
	Title nvarchar(27) NULL,
	Tutor nvarchar(30) NULL,
	Day nvarchar(3) NULL,
	Time nvarchar(15) NULL,
	Room nvarchar(10) NULL,
	Semester tinyint NULL,
)

--Create enrolments table
CREATE TABLE tblEnrolments(
	StudentNo int NOT NULL,
	GroupNo nvarchar(4) NOT NULL,
	assessGrade int,
	examGrade int
)

